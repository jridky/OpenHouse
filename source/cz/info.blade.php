<p>Hi and welcome to the Open House WorkAdventure map!</p>

<p>The platform is made to recreate the social experience of Open House in a game-like environment. You can walk around and explore Red Hat Brno office. You can meet people in the hallways and have a chat or join them by the tables and enjoy the snacks together.</p>

<p><strong>Q: How does this work? </strong></p>
<p><strong>A:</strong> You can move around pressing keys: W/A/S/D (or arrow keys), you can explore the map and most importantly you can connect with others!</strong></p>
<p><strong>Q: How do I connect with others? Walk up to them!</strong></p>
<p><strong>A:</strong> Every player has a circle around them, when you come into the circle a call will start. The hallway calls are <strong>limited to 4 people.</strong></p>
<p><strong>Q: Is there a participant limit for the call?</strong></p>
<p><strong>A:</strong> Yes, in hallways, regular rooms and outside you can only talk to upto 4 people. <strong> Meeting rooms with names have a bigger capacity.</strong></p>
<p><strong>Q: How can I connect with Red Hatters on the map?</strong></p>
<p><strong>A:</strong> If you are not sure who is a Red Hatter, there is a red stall in the main entrance of the building, if you walk up to it a call with a Red Hatter will start! When the Jitsi starts, the page will split in half, WorkAdventure on the left side and the call on the right side.</p>
